package com.njyb.gbdbas.util.translate;

/**
 * 语言翻译工具类
 * @author 洪皓
 * 2015-04-09
 * @version 标准版
 */
public class TransResult {

	public String getSrc() {
		return src;
	}

	public void setSrc(String src) {
		this.src = src;
	}

	public String getDst() {
		return dst;
	}

	public void setDst(String dst) {
		this.dst = dst;
	}

	private String src;
	private String dst;

}
